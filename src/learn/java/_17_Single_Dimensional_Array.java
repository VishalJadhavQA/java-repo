package learn.java;

public class _17_Single_Dimensional_Array {
	
	public static void main(String[] args) {
	
	int[] a = new int[3]; // Declare and create array
	
	a[0] = 2;		//Assigning values to array
	a[1] = 4;
	a[2] = 6;
	
	
	
	System.out.println(a[0]); // Accessing values of array
	System.out.println(a[1]);
	System.out.println(a[2]);
		

	System.out.println("*-*-*-*--*-*-*-*-*-*-*-*-*-*-*");
	
	/* Single Dimensional Array Shortcut */
	
	int[] b = {5,3,9};
	
	System.out.println(b[0]);
	System.out.println(b[1]);
	System.out.println(b[2]);
	
	
	
	

	}
}