package learn.java;

public class _08_Assignment_Operator {

	public static void main(String[] args) {
		int a = 5;
		int b = 5;
		int c = 10;
		int d =8;
		int e = 12;
		
		a+=4;//a=a+4  //a=5+4  //a=9
		System.out.println(a);//a=9
		
		b-=4;//b=b+4  //b=5+4  //b=9
		System.out.println(b);//b=9
		
		c*=2;//c=c*2 //c=10*2 //c=20
		System.out.println(c);//c=20
		
		d/=3;//d=d*3 //d=8/3 //d=2
		System.out.println(d);//d=2
		
		e/=5;//e=e*3 //e=8/3 //e=2
		System.out.println(e);//e=2
		

	}

}
